import math

resultado = math.floor(3.5)
print(resultado)  # 3

resultado = math.ceil(3.5)
print(resultado)  # 4

resultado = math.trunc(3.5)
print(resultado)  # 3

resultado = math.log(100,5)
print(resultado)  # 2.8613531161467867

resultado = math.log10(100)
print(resultado)  # 2.0

resultado = math.cos(0)
print(resultado)  # 1.0

resultado = math.sin(0)
print(resultado)  # 0.0

resultado = math.tan(0)
print(resultado)  # 0.0

resultado = math.degrees(0)
print(resultado)  # 0.0

resultado = math.radians(0)
print(resultado)  # 0.0

resultado = math.pi
print(resultado)  # 3.141592653589793

resultado = math.e
print(resultado)  # 2.718281828459045

resultado = math.inf
print(resultado)  # inf

resultado = math.nan
print(resultado)  # nan

resultado = math.isfinite(1)
print(resultado)  # True

resultado = math.isinf(1)
print(resultado)  # False

resultado = math.isnan(1)
print(resultado)  # False

resultado = math.sqrt(9)
print(resultado)  # 3.0

resultado = math.pow(2,3)
print(resultado)  # 8.0

resultado = math.exp(2)
print(resultado)  # 7.389056098930649

resultado = math.factorial(5)
print(resultado)  # 120

resultado = math.gcd(12, 15)
print(resultado)  # 3

resultado = math.fmod(10, 3)
print(resultado)  # 1.0

resultado = math.fabs(-3)
print(resultado)  # 3.0

resultado = math.copysign(-3, 2)
print(resultado)  # 3.0

resultado = math.copysign(3, -2)
print(resultado)  # -3.0